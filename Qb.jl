```
"""
function Qb(pt, Fc, M, Q, d, pw, σwy, b)
# Qb.jl
# 2021-01-23

  j = d * 7 / 8
  M/Q <  d && return ((0.068pt ^ 0.23 * (Fc + 18)) / (1 + 0.12) + 0.85 * sqrt(pw * σwy)) * b * j
  M/Q > 3d && return ((0.068pt ^ 0.23 * (Fc + 18)) / (3 + 0.12) + 0.85 * sqrt(pw * σwy)) * b * j
  return ((0.068pt ^ 0.23 * (Fc + 18)) / (M / (Q * d) + 0.12) + 0.85 * sqrt(pw * σwy)) * b * j
end
