"""
function Qw(pte, te, Fc, M, Q, D, pwh, σwh, σ0, j)
# function_qw.jl
# 2021-01-23

  if M / Q < D
    MoverQD = 1
  elseif M / Q > 3D
    MoverQD = 3
  end
  (0.068pte ^ 0.23 * (Fc + 18) / sqrt(MoverQD + 0.12) +
    0.85 * sqrt(pwh * σwh) + 0.1σ0) * te * j
end
